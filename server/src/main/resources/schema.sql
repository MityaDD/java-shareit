DROP TABLE IF EXISTS bookings, items, comments, users, requests CASCADE;

CREATE TABLE IF NOT EXISTS users (
user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(40) NOT NULL,
email VARCHAR(150) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS requests(
request_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
description varchar(100) NOT NULL,
created timestamp,
requester BIGINT REFERENCES users(user_id) ON DELETE CASCADE);

CREATE TABLE IF NOT EXISTS items (
item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name VARCHAR(40),
description VARCHAR(150) NOT NULL,
available BOOLEAN,
user_id BIGINT REFERENCES users(user_id) ON DELETE CASCADE,
request BIGINT REFERENCES requests(request_id) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS bookings (
booking_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
item_id BIGINT REFERENCES items(item_id) ON DELETE CASCADE,
start_date TIMESTAMP NOT NULL,
end_date TIMESTAMP NOT NULL,
booker_id BIGINT REFERENCES users(user_id) ON DELETE CASCADE,
status VARCHAR(10)
);

CREATE TABLE IF NOT EXISTS comments (
comment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
text VARCHAR(200) NOT NULL,
item_id BIGINT REFERENCES items(item_id) ON DELETE CASCADE,
author_name VARCHAR(40) NOT NULL,
created TIMESTAMP
);